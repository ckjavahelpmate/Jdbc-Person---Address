package com.ty.pa.dto;

public class Address {
	private int aid ;
	private String street ;
	private int doorno ;
	private String city ;
	private int pin ;
	private int pid ;
	public int getAid() {
		return aid;
	}
	public void setAid(int aid) {
		this.aid = aid;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}
	public int getDoorno() {
		return doorno;
	}
	public void setDoorno(int doorno) {
		this.doorno = doorno;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public int getPin() {
		return pin;
	}
	public void setPin(int pin) {
		this.pin = pin;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	@Override
	public String toString() {
		return "Address [aid=" + aid + ", street=" + street + ", doorno=" + doorno + ", city=" + city + ", pin=" + pin
				+ ", pid=" + pid + "]";
	}
	
	
}
